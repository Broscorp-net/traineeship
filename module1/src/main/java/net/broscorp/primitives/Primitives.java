package net.broscorp.primitives;

public class Primitives {

  public int add(int a, int b) {
    return a + b;
  }

  public int conversion(long a) {
    return (int) a;
  }

  public double conversion2(float a) {
    double b = a;
    return b;
  }

  public double count(double a,double b) {
    double number = 1;
    return number + a + b;
  }

}
